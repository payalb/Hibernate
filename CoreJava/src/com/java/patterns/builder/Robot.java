package com.java.patterns.builder;

public class Robot {

	private int arms;
	private int legs;
	private int torso;
	private int head;
	public int getArms() {
		return arms;
	}
	public void setArms(int arms) {
		this.arms = arms;
	}
	public int getLegs() {
		return legs;
	}
	public void setLegs(int legs) {
		this.legs = legs;
	}
	public int getTorso() {
		return torso;
	}
	public void setTorso(int torso) {
		this.torso = torso;
	}
	public int getHead() {
		return head;
	}
	public void setHead(int head) {
		this.head = head;
	}
}
